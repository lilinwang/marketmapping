                    <h2 class="section-heading">Market Mapping Best Practices</h2>
                    <p>There are three key decision points to effectively position your company in a new competitive landscape or industry.  Addressing these three decision points will help you decide how to best present a visualization of your market map.</p>
                    <ul style="list-style-type:disc">
                     <li>Differentiation: What are your company’s unique features that sets it apart from the competition, and why do customers prefer your solution?</li>
                     </br>
                     <li>Customers: Which customers segment is the focus for your product or service?  Instead of trying to please all people in all ways, please a specific subset of people — a defined segment of the market — in an extraordinary way with the unique attributes and experiences you offer.</li>
                     </br>
                     <li>Competition: Who are your competitors, and how does your offering fit within the competitive landscape?  Specifically:</li>
                        <ul>
                            <li>How do you think your offering ranks?
                            <li>How do your customers think your offering ranks?
                            <li>If these rankings are different, why?
                        </ul>
                    </ul> 
                    <p>In adherence with the best practices outlined above, we have created a unique and engaging user experience to entice industry experts – founders and investors – to contribute custom market maps which our product can transform into quantifiable, data-driven market maps.</p>

